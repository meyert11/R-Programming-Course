## These functions create a matrix object, and if its inverse is not cached from
## previous runs, it generates the inverse and caches it.

## makeCacheMatrix brings in the variabel x, creates its inverse and caches it.

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinv <- function(solve) m <<- solve
    getinv <- function() m
    list(set = set, get = get,
         setinv = setinv,
         getinv = getinv)
}


## This function reads in a matrix and checks whether a cache of its inverse
## exists, if not it creates it.

cacheSolve <- function(x, ...) {
    m <- x$getinv()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinv(m)
    m
}
